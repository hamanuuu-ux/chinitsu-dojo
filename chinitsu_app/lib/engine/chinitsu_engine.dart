/// 13枚の手牌から待ち牌リスト（1〜9）を返す。
/// 4枚使用済みの牌は待ちにならない。
List<int> calcWaits(List<int> tiles13) {
  final waits = <int>[];
  final cnt = List<int>.filled(10, 0);
  for (final t in tiles13) {
    cnt[t]++;
  }
  for (int w = 1; w <= 9; w++) {
    if (cnt[w] >= 4) continue; // 4枚使用済み→待ちにならない
    final h = [...tiles13, w]..sort();
    if (canWin(h)) waits.add(w);
  }
  return waits;
}

/// 14枚の手牌がアガリ形（雀頭1 + 面子4）かどうかを判定する。
/// 完全バックトラック方式。
bool canWin(List<int> h) {
  final c = List<int>.filled(10, 0);
  for (final t in h) {
    c[t]++;
  }
  final tried = <int>{};
  for (final t in h) {
    if (tried.contains(t)) continue;
    tried.add(t);
    if (c[t] >= 2) {
      c[t] -= 2;
      if (_solveMentsu(c, 4)) {
        c[t] += 2;
        return true;
      }
      c[t] += 2;
    }
  }
  return false;
}

/// 残りの面子を再帰的に取り出せるか判定する。
/// [c] はカウント配列（index 1〜9）、[rem] は残り面子数。
bool _solveMentsu(List<int> c, int rem) {
  if (rem == 0) {
    for (int i = 1; i <= 9; i++) {
      if (c[i] != 0) return false;
    }
    return true;
  }
  // 最小の牌番号を見つける
  int n = -1;
  for (int i = 1; i <= 9; i++) {
    if (c[i] > 0) {
      n = i;
      break;
    }
  }
  if (n < 0) return false;

  // 刻子（同じ牌3枚）
  if (c[n] >= 3) {
    c[n] -= 3;
    if (_solveMentsu(c, rem - 1)) {
      c[n] += 3;
      return true;
    }
    c[n] += 3;
  }

  // 順子（連続する3枚）
  if (n <= 7 && c[n] >= 1 && c[n + 1] >= 1 && c[n + 2] >= 1) {
    c[n]--;
    c[n + 1]--;
    c[n + 2]--;
    if (_solveMentsu(c, rem - 1)) {
      c[n]++;
      c[n + 1]++;
      c[n + 2]++;
      return true;
    }
    c[n]++;
    c[n + 1]++;
    c[n + 2]++;
  }

  return false;
}

/// 14枚の手牌を雀頭+面子4に分解し、全パターンを返す。
/// 各パターンは [head, mentsu1, mentsu2, mentsu3, mentsu4] のリスト。
/// head は [int, int]、mentsu は [int, int, int]。
List<List<List<int>>> decomposeHand(List<int> tiles14) {
  final c = List<int>.filled(10, 0);
  for (final t in tiles14) {
    c[t]++;
  }
  final results = <List<List<int>>>[];
  final tried = <int>{};

  for (final t in tiles14) {
    if (tried.contains(t)) continue;
    tried.add(t);
    if (c[t] >= 2) {
      c[t] -= 2;
      final mentsuList = <List<int>>[];
      _findAllDecompositions(c, 4, mentsuList, results, [t, t]);
      c[t] += 2;
    }
  }
  return results;
}

void _findAllDecompositions(
  List<int> c,
  int rem,
  List<List<int>> current,
  List<List<List<int>>> results,
  List<int> head,
) {
  if (rem == 0) {
    bool allZero = true;
    for (int i = 1; i <= 9; i++) {
      if (c[i] != 0) {
        allZero = false;
        break;
      }
    }
    if (allZero) {
      results.add([head, ...current]);
    }
    return;
  }

  int n = -1;
  for (int i = 1; i <= 9; i++) {
    if (c[i] > 0) {
      n = i;
      break;
    }
  }
  if (n < 0) return;

  // 刻子
  if (c[n] >= 3) {
    c[n] -= 3;
    current.add([n, n, n]);
    _findAllDecompositions(c, rem - 1, current, results, head);
    current.removeLast();
    c[n] += 3;
  }

  // 順子
  if (n <= 7 && c[n] >= 1 && c[n + 1] >= 1 && c[n + 2] >= 1) {
    c[n]--;
    c[n + 1]--;
    c[n + 2]--;
    current.add([n, n + 1, n + 2]);
    _findAllDecompositions(c, rem - 1, current, results, head);
    current.removeLast();
    c[n]++;
    c[n + 1]++;
    c[n + 2]++;
  }
}

