final _rng = Random();

const _maxAttempts = 100;
const _sequenceRatio = 0.65;
const _tenpaiRatio = 0.7;

/// 完成形（4面子+1雀頭=14枚）をランダム生成する。
List<int>? generateCompleteHand() {
  for (int attempt = 0; attempt < _maxAttempts; attempt++) {
    final tiles = <int>[];
    final c = List<int>.filled(10, 0);

    // 雀頭
    final hv = 1 + _rng.nextInt(9);
    if (c[hv] + 2 > 4) continue;
    tiles.addAll([hv, hv]);
    c[hv] += 2;

    bool ok = true;
    for (int mi = 0; mi < 4; mi++) {
      final isSequence = _rng.nextDouble() < _sequenceRatio;
      if (isSequence) {
        final st = 1 + _rng.nextInt(7);
        if (c[st] + 1 <= 4 && c[st + 1] + 1 <= 4 && c[st + 2] + 1 <= 4) {
          tiles.addAll([st, st + 1, st + 2]);
          c[st]++;
          c[st + 1]++;
          c[st + 2]++;
        } else {
          ok = false;
          break;
        }
      } else {
        final v = 1 + _rng.nextInt(9);
        if (c[v] + 3 <= 4) {
          tiles.addAll([v, v, v]);
          c[v] += 3;
        } else {
          ok = false;
          break;
        }
      }
    }
    if (ok && tiles.length == 14) {
      tiles.sort();
      return tiles;
    }
  }
  return null;
}

/// 完成形から1枚抜いてテンパイ形（13枚）を作る。
List<int>? generateTenpaiHand() {
  for (int attempt = 0; attempt < 50; attempt++) {
    final hand = generateCompleteHand();
    if (hand == null) continue;

    // インデックスをシャッフルして順に試す
    final indices = List<int>.generate(14, (i) => i);
    indices.shuffle(_rng);

    for (final idx in indices) {
      final t13 = [...hand.sublist(0, idx), ...hand.sublist(idx + 1)];
      if (calcWaits(t13).isNotEmpty) {
        t13.sort();
        return t13;
      }
    }
  }
  return null;
}

/// 完全ランダムな13枚を生成する（テンパイかどうかは不問）。
List<int> generateRandomHand() {
  final tiles = <int>[];
  final c = List<int>.filled(10, 0);
  int i = 0;
  while (i < 13) {
    final v = 1 + _rng.nextInt(9);
    if (c[v] >= 4) continue;
    tiles.add(v);
    c[v]++;
    i++;
  }
  tiles.sort();
  return tiles;
}

/// 問題を1問生成する（70%テンパイ、30%ランダム）。
List<int> generateProblem() {
  if (_rng.nextDouble() < _tenpaiRatio) {
    final h = generateTenpaiHand();
    if (h != null) return h;
  }
  return generateRandomHand();
}

